import UIKit

extension UIColor {
    final class func xpGray() -> UIColor {
        return UIColor(hex: 0xf7f7f7)
    }
    
    final class func xpTextGray() -> UIColor {
        return UIColor(hex: 0xb2b2b2)
    }
    
    final class func xpLightDarkGray() -> UIColor {
        return UIColor(hex: 0xe9e9e9)
    }
    
    final class func xpLightGray() -> UIColor {
        return UIColor(hex: 0xfbfbfb)
    }
    
    final class func xpSeparatorGray() -> UIColor {
        return UIColor(hex: 0xc7c7cc)
    }
    
    final class func xpBlue() -> UIColor {
        return UIColor(hex: 0x1ca8fd)
    }
    
    final class func xpYellow() -> UIColor {
        return UIColor(hex: 0xfffc00)
    }
    
    final class func xpDarkGray() -> UIColor {
        return UIColor(hex: 0xb2b2b2)
    }
    
}
